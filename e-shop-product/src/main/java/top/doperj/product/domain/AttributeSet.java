package top.doperj.product.domain;

public class AttributeSet {
    private Integer attributeSetId;

    private Integer attributeSetName;

    public Integer getAttributeSetId() {
        return attributeSetId;
    }

    public void setAttributeSetId(Integer attributeSetId) {
        this.attributeSetId = attributeSetId;
    }

    public Integer getAttributeSetName() {
        return attributeSetName;
    }

    public void setAttributeSetName(Integer attributeSetName) {
        this.attributeSetName = attributeSetName;
    }
}